# Calculate benchmark averages

w_av <- function(y, q, m, w, d, h){
  w1 <- (23000/100000)*y
  w2 <- (24000/100000)*q
  w3 <- (48000/100000)*m
  w4 <- (359  /100000)*w
  w5 <- (4227 /100000)*d
  w6 <- (414  /100000)*h
  output <- round( (w1+w2+w3+w4+w5+w6),4)
  return(output)
}

